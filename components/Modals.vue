          try {
            await this.$firebase
              .db
              .ref(`users/${this.user.email}`)
              .set({
                first_name: this.newName,
                email: this.user.email
              })
          } catch (err) {
            console.error('exist already', err)
          }
          this.$modal.hide('loading')
          this.$router.push('/makers')
        })
        .catch(() => {
          this.$modal.hide('loading')
        })
    },
